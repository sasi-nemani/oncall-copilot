"""Regenerate the README's "Latest verified run" block from REAL eval runs.

Runs the deterministic retrieval suite always, and the agent eval when an API key for
the configured roles is available. Rewrites README.md between the EVAL markers — so the
published numbers are always outputs of an actual run, never hand-edited.

Usage:  python -m evals.report              # retrieval only (keyless)
        python -m evals.report --agent      # + full agent eval (needs the role keys)
"""
import argparse
import datetime
import os
import re
import sys

ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
README = os.path.join(ROOT, "README.md")
START, END = "<!-- EVAL:START -->", "<!-- EVAL:END -->"


def build_block(agent=False):
    from evals import retrieval_compare
    today = datetime.date.today().isoformat()
    lines = [START,
             f"_Generated by `python -m evals.report` on {today} — these numbers are the "
             "output of a real run, not hand-edited._", ""]

    kw = retrieval_compare.score("keyword")
    row_hybrid = ""
    try:                                   # hybrid needs sentence-transformers (optional)
        hy = retrieval_compare.score("hybrid")
        if hy["found"] != kw["found"]:     # fell back to keyword if embeddings absent
            row_hybrid = f"| retrieval recall@4 (hybrid) | {hy['found']}/{hy['total']} = {hy['rate']:.0%} |\n"
    except Exception:
        pass

    lines += ["| Suite | Result |", "|---|---|",
              f"| retrieval recall@4 (keyword) | {kw['found']}/{kw['total']} = {kw['rate']:.0%} |"]
    if row_hybrid:
        lines.append(row_hybrid.rstrip())

    if agent:
        from evals import run_evals
        out = run_evals.run()
        for k, v in out["dims"].items():
            lines.append(f"| agent {k.replace('_', '-')} | {v:.0%} |")
        lines.append(f"| **agent pass rate (n={out['n']})** | **{out['passed']}/{out['n']} = "
                     f"{out['rate']:.0%}** — gate {'OPEN' if out['rate'] >= 0.8 else 'BLOCKED'} |")
        lines.append(f"\n_Agent run: answerer `{out['answerer']}`, judge `{out['judge']}`, "
                     f"mode `{out['mode']}`"
                     + (f", {out['errored']} case(s) errored_" if out["errored"] else "_"))
    else:
        lines.append("| agent suites | _not run (needs an API key — `python -m evals.report --agent`)_ |")

    lines.append(END)
    return "\n".join(lines)


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--agent", action="store_true", help="also run the full agent eval")
    ap.add_argument("--stdout", action="store_true", help="print the block, don't touch README")
    args = ap.parse_args()

    block = build_block(agent=args.agent)
    if args.stdout:
        print(block)
        return
    text = open(README, encoding="utf-8").read()
    if START not in text or END not in text:
        sys.exit(f"README is missing the {START} / {END} markers")
    new = re.sub(re.escape(START) + r".*?" + re.escape(END), block, text, flags=re.S)
    open(README, "w", encoding="utf-8").write(new)
    print(f"README updated between EVAL markers ({'retrieval + agent' if args.agent else 'retrieval only'}).")


if __name__ == "__main__":
    main()
